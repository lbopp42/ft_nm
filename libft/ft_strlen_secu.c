#include "libft.h"

size_t	ft_strlen_secu(const char *s, void *eos)
{
	size_t i;

	i = 0;
	while (s[i] != '\0')
	{
		i++;
		if ((void*)(&s[i]) >= eos)
			return (0);
	}
	return (i);
}
